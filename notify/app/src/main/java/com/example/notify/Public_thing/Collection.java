package com.example.notify.Public_thing;

public class Collection {
    public String tel;
    public String name;
    public String title;
    public String text;
    public String image;
    public String datetime;

    public Collection(String tel, String name, String title, String text, String image, String datetime) {
        this.tel = tel;
        this.name = name;
        this.title = title;
        this.text = text;
        this.image = image;
        this.datetime = datetime;
    }

}
